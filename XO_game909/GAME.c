/************************************************************************************
 * Function Name: ButtonsInit
 * Function Inputs : none
 * Description: Initialize the directional buttons
 ************************************************************************************/
void ButtonsInit(){
		/*Set Pinns direction from PF0 to PF4  as an INPUT bins*/
		GPIO_SetPinDirection(GPIOF, PIN0, INPUT);//enter button
		GPIO_SetPinDirection(GPIOF, PIN1, INPUT);//up button
		GPIO_SetPinDirection(GPIOF, PIN2, INPUT);//down button
		GPIO_SetPinDirection(GPIOF, PIN3, INPUT);//left button
		GPIO_SetPinDirection(GPIOF, PIN4, INPUT);//right button

		/*Set Pull up resistor for pins from PF0 to PF4*/
		GPIO_PullUp(GPIOF, PIN0, 1);
		GPIO_PullUp(GPIOF, PIN1, 1);
		GPIO_PullUp(GPIOF, PIN2, 1);
		GPIO_PullUp(GPIOF, PIN3, 1);
		GPIO_PullUp(GPIOF, PIN4, 1);

    /*Enable digital pins from PF0 to PF4*/
		GPIO_DigitalEnable(GPIOF,PIN0, ENABLE );
		GPIO_DigitalEnable(GPIOF,PIN1, ENABLE );
		GPIO_DigitalEnable(GPIOF,PIN2, ENABLE );
		GPIO_DigitalEnable(GPIOF,PIN3, ENABLE );
		GPIO_DigitalEnable(GPIOF,PIN4, ENABLE );
	
		/*Set Interrupt sense to edge*/
		GPIO_SelectInterruptSense(GPIOF, PIN0, SENSE_EDGE);
		GPIO_SelectInterruptSense(GPIOF, PIN1, SENSE_EDGE);
		GPIO_SelectInterruptSense(GPIOF, PIN2, SENSE_EDGE);
		GPIO_SelectInterruptSense(GPIOF, PIN3, SENSE_EDGE);
		GPIO_SelectInterruptSense(GPIOF, PIN4, SENSE_EDGE);
	
		/*Clear Interrupt from pins */
		GPIO_ClearInterrupt(GPIOF, PIN0);
		GPIO_ClearInterrupt(GPIOF, PIN1);
		GPIO_ClearInterrupt(GPIOF, PIN2);
		GPIO_ClearInterrupt(GPIOF, PIN3);
		GPIO_ClearInterrupt(GPIOF, PIN4);
    
		/*Enable Interrupt to pins*/
		GPIO_EnableInterrupt(GPIOF, PIN0);
		GPIO_EnableInterrupt(GPIOF, PIN1);
		GPIO_EnableInterrupt(GPIOF, PIN2);
		GPIO_EnableInterrupt(GPIOF, PIN3);		
		GPIO_EnableInterrupt(GPIOF, PIN4);
	
		/*Select the falling edge as an interrupt event*/
		GPIO_SelectInterruptEvent(GPIOF, PIN0, FALLING_EDGE );
		GPIO_SelectInterruptEvent(GPIOF, PIN1, FALLING_EDGE );
		GPIO_SelectInterruptEvent(GPIOF, PIN2, FALLING_EDGE );
		GPIO_SelectInterruptEvent(GPIOF, PIN3, FALLING_EDGE );
		GPIO_SelectInterruptEvent(GPIOF, PIN4, FALLING_EDGE );
	  
		/* Disable interrupt on both edges */
    GPIO_InterruptBothEdges(GPIOF, PIN0, SINGLE_EDGE);
    GPIO_InterruptBothEdges(GPIOF, PIN1, SINGLE_EDGE);
    GPIO_InterruptBothEdges(GPIOF, PIN2, SINGLE_EDGE);
    GPIO_InterruptBothEdges(GPIOF, PIN3, SINGLE_EDGE);
    GPIO_InterruptBothEdges(GPIOF, PIN4, SINGLE_EDGE);
}


